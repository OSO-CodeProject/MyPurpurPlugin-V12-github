package org.example.config;

/** Определяет политику вступления игроков в команды. */
public enum JoinMode {
  /** Игроки вступают сразу после команды /team join. */
  OPEN,

  /** Вступление возможно только по приглашению лидера. */
  INVITE_ONLY,

  /** Игроки отправляют заявку и ждут подтверждения лидера. */
  REQUEST_TO_JOIN;
}
